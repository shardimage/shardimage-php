namespace shardimage\shardimagephp\loggers;

use Psr\Log\AbstractLogger;
use shardimage\shardimagephp\helpers\LoggerHelper;

abstract class BaseLogger extends AbstractLogger
{
    /**
     * Interpolates context values into the message placeholders.
     */
    protected function interpolate($message, array $context = [])
    {
        // build a replacement array with braces around the context keys
        $replace = [];
        foreach ($context as $key => $val) {
            // check that the value can be casted to string
            if (!is_array($val) && (!is_object($val) || method_exists($val, '__toString'))) {
                $replace['{' . $key . '}'] = $val;
            }
        }

        // interpolate replacement values into the message and return
        return strtr(LoggerHelper::replaceBinaryStrings((string) $message), $replace);
    }
}
